    public static void printfile(Data data) {
//        for (String s : data.names) {
//            System.out.print(s + "\t");
//        }
        System.out.println("");
        for (ArrayList<Object> s : data.lines) {
            for (Object d : s) {
                if (d instanceof Double) {
                    System.out.print(((Double) d) + "\t");
                } else if (d instanceof Integer) {
                    System.out.print(((Integer) d) + "\t");
                }
            }
            System.out.println("");
        }
    }

